/// The full fixed palette backing a [`CategoricalColormap`].
///
/// Useful for uploading the whole scheme at once (e.g. emitting it as a WGSL
/// `const` array so a shader can sample categorical colors on the GPU), rather
/// than sampling one index at a time via [`sample`].
pub fn palette(colormap: CategoricalColormap) -> &'static [Rgba] {
    match colormap {
        CategoricalColormap::Accent => &ACCENT,
        CategoricalColormap::Category10 => &CATEGORY10,
        CategoricalColormap::Category20 => &CATEGORY20,
        CategoricalColormap::Category20b => &CATEGORY20B,
        CategoricalColormap::Category20c => &CATEGORY20C,
        CategoricalColormap::Observable10 => &OBSERVABLE10,
        CategoricalColormap::Dark2 => &DARK2,
        CategoricalColormap::Paired => &PAIRED,
        CategoricalColormap::Pastel1 => &PASTEL1,
        CategoricalColormap::Pastel2 => &PASTEL2,
        CategoricalColormap::Set1 => &SET1,
        CategoricalColormap::Set2 => &SET2,
        CategoricalColormap::Set3 => &SET3,
        CategoricalColormap::Tableau10 => &TABLEAU10,
        CategoricalColormap::Tableau20 => &TABLEAU20,
    }
}

/// Sample a [`CategoricalColormap`] at category index `i`, wrapping around
/// (via modulo) once `i` exceeds the palette's length.
pub fn sample(colormap: CategoricalColormap, i: usize) -> Rgba {
    match colormap {
        CategoricalColormap::Accent => accent(i),
        CategoricalColormap::Category10 => category10(i),
        CategoricalColormap::Category20 => category20(i),
        CategoricalColormap::Category20b => category20b(i),
        CategoricalColormap::Category20c => category20c(i),
        CategoricalColormap::Observable10 => observable10(i),
        CategoricalColormap::Dark2 => dark2(i),
        CategoricalColormap::Paired => paired(i),
        CategoricalColormap::Pastel1 => pastel1(i),
        CategoricalColormap::Pastel2 => pastel2(i),
        CategoricalColormap::Set1 => set1(i),
        CategoricalColormap::Set2 => set2(i),
        CategoricalColormap::Set3 => set3(i),
        CategoricalColormap::Tableau10 => tableau10(i),
        CategoricalColormap::Tableau20 => tableau20(i),
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn first_entries_match_control_points() {
        assert_eq!(accent(0), rgb(0x7f, 0xc9, 0x7f));
        assert_eq!(category10(0), rgb(0x1f, 0x77, 0xb4));
        assert_eq!(tableau20(0), rgb(0x4c, 0x78, 0xa8));
    }

    #[test]
    fn last_entries_match_control_points() {
        assert_eq!(accent(7), rgb(0x66, 0x66, 0x66));
        assert_eq!(category10(9), rgb(0x17, 0xbe, 0xcf));
        assert_eq!(tableau20(19), rgb(0xd8, 0xb5, 0xa5));
    }

    #[test]
    fn index_wraps_around_palette_length() {
        assert_eq!(dark2(8), dark2(0));
        assert_eq!(category10(10), category10(0));
        assert_eq!(category10(13), category10(3));
    }

    #[test]
    fn sample_dispatches_to_matching_function() {
        assert_eq!(sample(CategoricalColormap::Set1, 2), set1(2));
        assert_eq!(sample(CategoricalColormap::Paired, 5), paired(5));
    }
}
